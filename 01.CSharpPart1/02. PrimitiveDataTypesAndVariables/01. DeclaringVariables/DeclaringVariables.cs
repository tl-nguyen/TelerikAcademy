using System;

class DeclaringVariables
{
    static void Main()
    {
        ushort variableOne = 52130;
        sbyte variableTwo = -115;
        int variableThree = 4825932;
        byte variableFour = 97;
        short variableFive = -10000;

        Console.WriteLine("{0} ; {1} ; {2} ; {3} ; {4} ", variableOne, variableTwo, variableThree, variableFour, variableFive);
    }
}

