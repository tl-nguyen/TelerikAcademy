using System;

class FloatOrDouble
{
    static void Main()
    {
        double firstNumber = 34.567839023;
        float secondNumber = 12.345f;
        double thirdNumber = 8923.1234857f;
        float fourthNumber = 3456.091f;

        Console.WriteLine("Float values are : {0} ; {1}",secondNumber, fourthNumber);
        Console.WriteLine("Double values are : {0} ; {1}", firstNumber, thirdNumber);
    }
}

